<script setup lang="ts">
import Chart from 'chart.js/auto'
import { onMounted } from 'vue'

interface Props {
  id: string
  height?: string
  title?: string
  description?: string
  chart: {
    labels: any[]
    datasets: {
      label: string
      data: any[]
    }[]
  }
}

const props = withDefaults(defineProps<Props>(), {
  height: '300',
  title: '',
  description: '',
})

onMounted(() => {
  const gradientLineChart = (document.getElementById(props.id) as HTMLCanvasElement)?.getContext('2d')

  const gradientStroke1 = gradientLineChart?.createLinearGradient(0, 230, 0, 50)

  gradientStroke1?.addColorStop(1, 'rgba(203,12,159,0.2)')
  gradientStroke1?.addColorStop(0.2, 'rgba(72,72,176,0.0)')
  gradientStroke1?.addColorStop(0, 'rgba(203,12,159,0)') // purple colors

  const gradientStroke2 = gradientLineChart?.createLinearGradient(0, 230, 0, 50)

  gradientStroke2?.addColorStop(1, 'rgba(20,23,39,0.2)')
  gradientStroke2?.addColorStop(0.2, 'rgba(72,72,176,0.0)')
  gradientStroke2?.addColorStop(0, 'rgba(20,23,39,0)') // purple colors

  const chartStatus = Chart.getChart(props.id)
  if (chartStatus !== undefined) {
    chartStatus.destroy()
  }
  if (props.chart.datasets.length === 2) {
    new Chart(gradientLineChart!, {
      type: 'line',
      data: {
        labels: props.chart.labels,
        datasets: [
          {
            label: props.chart.datasets[0].label,
            tension: 0.4,
            pointRadius: 0,
            borderColor: '#4BB543 ',
            backgroundColor: gradientStroke1,
            borderWidth: 3,
            fill: true,
            data: props.chart.datasets[0].data,
          },
          {
            label: props.chart.datasets[1].label,
            tension: 0.4,
            pointRadius: 0,
            borderColor: '#3A416F',
            borderWidth: 3,
            backgroundColor: gradientStroke2,
            fill: true,
            data: props.chart.datasets[1].data,
          },
        ],
      },
      options: {
        responsive: true,
        maintainAspectRatio: false,
        plugins: {
          legend: {
            display: false,
          },
        },
        interaction: {
          intersect: false,
          mode: 'index',
        },
        scales: {
          y: {
            grid: {
              display: true,
              drawOnChartArea: true,
              drawTicks: false,
            },
            ticks: {
              display: true,
              padding: 10,
              color: '#fbfbfb',
              font: {
                size: 11,
                family: 'Open Sans',
                style: 'normal',
                lineHeight: 2,
              },
            },
          },
          x: {
            grid: {
              display: false,
              drawOnChartArea: false,
              drawTicks: false,
            },
            ticks: {
              display: true,
              color: '#ccc',
              padding: 20,
              font: {
                size: 11,
                family: 'Open Sans',
                style: 'normal',
                lineHeight: 2,
              },
            },
          },
        },
      },
    })
  }
  else if (props.chart.datasets.length === 1) {
    new Chart(gradientLineChart!, {
      type: 'line',
      data: {
        labels: props.chart.labels,
        datasets: [
          {
            label: props.chart.datasets[0].label,
            tension: 0.4,
            pointRadius: 0,
            borderColor: '#4BB543 ',
            backgroundColor: gradientStroke1,
            borderWidth: 3,
            fill: true,
            data: props.chart.datasets[0].data,
          },
        ],
      },
      options: {
        responsive: true,
        maintainAspectRatio: false,
        plugins: {
          legend: {
            display: false,
          },
        },
        interaction: {
          intersect: false,
          mode: 'index',
        },
        scales: {
          y: {
            grid: {
              display: true,
              drawOnChartArea: true,
              drawTicks: false,
            },
            ticks: {
              display: true,
              padding: 10,
              color: '#fbfbfb',
              font: {
                size: 11,
                family: 'Open Sans',
                style: 'normal',
                lineHeight: 2,
              },
            },
          },
          x: {
            grid: {
              display: false,
              drawOnChartArea: false,
              drawTicks: false,
            },
            ticks: {
              display: true,
              color: '#ccc',
              padding: 20,
              font: {
                size: 11,
                family: 'Open Sans',
                style: 'normal',
                lineHeight: 2,
              },
            },
          },
        },
      },
    })
  }
})
</script>

<template>
  <div class="card z-index-2">
    <div class="pb-0 card-header mb-0">
      <h6>{{ props.title }}</h6>
      <!--  eslint-disable-next-line vue/no-v-html -->
      <p v-if="props.description" class="text-sm" v-html="props.description" />
    </div>
    <div class="p-3 card-body">
      <div class="chart">
        <canvas :id="props.id" class="chart-canvas" :height="props.height" />
      </div>
    </div>
  </div>
</template>
